pub fn string_to_bigram(s: &str) -> Option<[char; 2]> {
  let chars: Vec<char> = s.chars().collect();
  if chars.len() == 2 {
    Some([chars[0], chars[1]])
  } else {
    None
  }
}

pub fn bigram_to_string(bigram: [char; 2]) -> String {
  bigram.iter().collect()
}
